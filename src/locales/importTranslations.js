const isPlainObject = (value) => value && typeof value === 'object' && !Array.isArray(value);

const mergeLocale = (base, overrides) => {
  const merged = { ...base };

  Object.entries(overrides).forEach(([key, value]) => {
    if (isPlainObject(value) && isPlainObject(base?.[key])) {
      merged[key] = mergeLocale(base[key], value);
    } else {
      merged[key] = value;
    }
  });

  return merged;
};

const fillMissingLocale = (base, fallback) => {
  if (!isPlainObject(fallback)) {
    return base === undefined ? fallback : base;
  }

  const resolvedBase = isPlainObject(base) ? base : {};
  const merged = { ...resolvedBase };

  Object.entries(fallback).forEach(([key, value]) => {
    if (isPlainObject(value)) {
      merged[key] = fillMissingLocale(resolvedBase[key], value);
      return;
    }

    if (merged[key] === undefined) {
      merged[key] = value;
    }
  });

  return merged;
};

const pick = (source, keys) =>
  keys.reduce((acc, key) => {
    if (source?.[key] !== undefined) {
      acc[key] = source[key];
    }
    return acc;
  }, {});

